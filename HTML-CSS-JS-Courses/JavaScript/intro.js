// DATA TYPES

// 1. Numbers

console.log(2019); // whole number

console.log(3.14); // decimal number

// 2. Strings

console.log('Hello, World!');

console.log('It was love at first sight.');

// 3. Booleans


console.log(true); // prints true


console.log(false); // prints false


// 4. Undefined

console.log(undefined); // prints undefined

// 5. Null

console.log(null); // prints null

// 6. Arrays

console.log([1, 2, 3, 4, 5]); // prints [1, 2, 3, 4, 5]


// 7. Objects

console.log({name: 'Yossarian', age: 25, isMarried: false}); // prints {name: 'Yossarian', age: 25, isMarried: false}

// 8. Functions

console.log(function hello() { console.log('Hello, World!'); }); // prints function hello() { console.log('Hello, World!'); }


// OPERATORS

// Arithmetic operators

console.log(1 + 1); // prints 2

console.log(2 - 1); // prints 1

console.log(2 * 2); // prints 4

console.log(4 / 2); // prints 2

console.log(5 % 2); // prints 1

// Comparison operators

console.log(1 === 1); // prints true

console.log(1 !== 1); // prints false

console.log(1 < 1); // prints false

console.log(1 <= 1); // prints true

console.log(1 > 1); // prints false

console.log(1 >= 1); // prints true

// Logical operators

console.log(true && true); // prints true

console.log(true || false); // prints true

console.log(!true); // prints false

// String concatenation

console.log('Hello, ' + 'World!'); // prints Hello, World!